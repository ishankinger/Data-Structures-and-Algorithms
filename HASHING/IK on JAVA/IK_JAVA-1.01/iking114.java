import java.util.*;

public class iking114 {
    public static void main(String args[]){
        Scanner sc= new Scanner(System.in);
                                                      //FORMAT OF INPUT 
        int n = sc.nextInt();

        int k =0;
        for(int i=0;i<=n;i++){
        k=k+i;                                       //ONE AFTER OTHER I IS ADDED IN VALUE OF K
    } 
       
        System.out.println(k);                      // NET LAST K WILL BE PRINTED
    
    
}
}
