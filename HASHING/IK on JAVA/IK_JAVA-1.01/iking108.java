import java.util.*;

public class iking108 {
    public static void main(String args[]){
        Scanner sc= new Scanner(System.in);
        int button = sc.nextInt();

        switch (button) {                                               // SWITCH( NAME){
        
        
        case 89 : System.out.println("hello");                       // CASE VALUE TO VARIABLE: CODE//
                                                                       // BREAK;
            break;

        case 97 : System.out.println("namaste");
          
            break;

        case 3 : System.out.println("bonjour");
            
            break;
           
         default: System.out.println("not valid");
          
            break;

    }
}

    
}
