import java.util.*;

public class iking116 {

    public static void main(String args[]){
       Scanner sc= new Scanner(System.in);
        int n= sc.nextInt();
        int m= sc.nextInt();

        for(int i=1;i<=n;i++){                                 //OUTER LOOP (for rows)
            
                for(int j=1;j<=m;j++){                         //INNER LOOP  (for colms)
                    System.out.print("*");             
                }
                System.out.println();                          //WAY TO WRITE THIS
                                                               // INTERNAL NESTED FOR LOOPS ARE WRITTEN LIKE THIS
            }                                                  // THIS REPRESENT INSIDE THE FOR LOOP
        }
}
                
             
        
    


