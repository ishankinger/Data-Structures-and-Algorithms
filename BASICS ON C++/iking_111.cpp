// WHILE LOOPS

#include<iostream>
using namespace std;

int main(){
    int i=1;                                      // INITIALISATION
    while(i<11){                                  // TILL CONDITION IS VALID WE HAVE TO GO IN LOOP
        cout<<i<<", ";                            // CODE IN WHILE LOOP
        i++;                                      // UPDATION IN THE LOOP
    }
    cout<<i<<".\n";
    return 0;
}