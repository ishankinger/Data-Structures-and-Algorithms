// TAKING INPUTS FROM THE USER

#include<iostream>
using namespace std;

int main(){
    int a;                                                         // FIRST DEFINE TYPE OF VARIABLE TO BE USED
    cout<<"type first number : ";
    cin>>a;                                                        // WAY TO WRITE TO TAKE INPUT FROM THE USER
    int b;
    cout<<"type second number : ";
    cin>>b;
    cout<<"addition of the two numbers is : "<<a+b;                // CONSECUTIVELY USING " " AND VARIABLES IN THE COUT
    return 0;
    
}