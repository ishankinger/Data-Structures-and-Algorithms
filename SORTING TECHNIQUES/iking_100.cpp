// -> By recursion : Sorting algorithms are either recursive [quick sort] or non-recursive [selection sort, and insertion
// /                  sort], and there are some algorithms which use both (merge sort).

// -> By stability : Few sorting algorithms maintain the relative order of elements with equal keys (equivalent
// /                 elements retain their relative positions even after sorting).

// -> By adaptability : With a few sorting algorithms, the complexity changes based on pre-sortedness [quick sort]:
// /                   presortedness of the input affects the running time. Algorithms that take this into account are known to be adaptive.

// -> Internal Sort : Sort algorithms that use main memory exclusively during the sort are called internal sorting
// /                  algorithms. This kind of algorithm assumes high-speed random access to all memory.

// -> External Sort : Sorting algorithms that use external memory, such as tape or disk, during the sort come under this
// /                  category.
